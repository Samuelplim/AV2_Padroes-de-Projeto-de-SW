package entities;

public class Barca {
    private int tamanho;
    private Acompanhamentos acompanhamentos;

    public Barca() {
    }

    public int getTamanho() {
        return tamanho;
    }

    public void setTamanho(int tamanho) {
        this.tamanho = tamanho;
    }

    public Acompanhamentos getAcompanhamentos() {
        return acompanhamentos;
    }

    public void setAcompanhamentos(Acompanhamentos acompanhamentos) {
        this.acompanhamentos = acompanhamentos;
    }
    
}
